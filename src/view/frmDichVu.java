    private void TongDV (){
        DecimalFormat x = new DecimalFormat("###,###,###,###,###,###");
        int TongDV = 0;
        for(int i=0; i< tblDV.getRowCount(); i++){
            TongDV+=Integer.parseInt(tblDV.getValueAt(i, 5).toString());
        }
        llbDV.setText("Tổng tiền dịch vụ là:"+" "+x.format(TongDV)+" "+"VNĐ");
    }
    
