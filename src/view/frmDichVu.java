    private void tblDVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDVMouseClicked
        try{
            int row = this.tblDV.getSelectedRow();
            String IDrow = (this.tblDV.getModel().getValueAt(row, 0)).toString();
            String sql = "SELECT * FROM tblDichVu where MaDV='"+IDrow+"'";
            ResultSet rs = UpdateTable.ShowTextField(sql);
            if(rs.next()) {
                this.txtTienDien.setText(rs.getString("TienDien"));
                this.txtTienNuoc.setText(rs.getString("TienNuoc"));
                this.txtTuNgay.setText(rs.getString("TuNgay"));
                this.txtDenNgay.setText(rs.getString("DenNgay"));
                this.txtTongTien.setText(rs.getString("TongTien"));
            }
        }catch(SQLException e) {
        } 
    }//GEN-LAST:event_tblDVMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
 
        if (this.txtMaDV.getText().length()==0 ) JOptionPane.showMessageDialog(null, "Mã dịch vụ không thể bỏ trống", "thông báo", 2);
            else {
            DichVu d = new DichVu(
                    Integer.parseInt(this.txtMaDV.getText()), 
                    Integer.parseInt(this.txtTienDien.getText()), 
                    Integer.parseInt(this.txtTienNuoc.getText()),
                    Date.valueOf(this.txtTuNgay.getText()),
                    Date.valueOf(this.txtDenNgay.getText()),
                    Integer.parseInt(this.txtTongTien.getText()));
            UpdateTable.LoadData(sqlDg, tblDV);
            try {
                DichVuData.UpdateDichVu(d);
                UpdateTable.LoadData(sqlDg, tblDV);
            } catch (SQLException ex) {
                Logger.getLogger(frmDichVu.class.getName()).log(Level.SEVERE, null, ex);
                UpdateTable.LoadData(sqlDg, tblDV);
            }
        resetForm();
        UpdateTable.LoadData(sqlDg, tblDV);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        if (this.txtMaDV.getText().length()==0 ) JOptionPane.showMessageDialog(null, "Mã dịch vụ không thể bỏ trống", "thông báo", 2);
            else {
            DichVu p = new DichVu(
                    Integer.parseInt(this.txtMaDV.getText()), 
                    Integer.parseInt(this.txtTienDien.getText()), 
                    Integer.parseInt(this.txtTienNuoc.getText()),
                    Date.valueOf(this.txtTuNgay.getText()),
                    Date.valueOf(this.txtDenNgay.getText()),
                    Integer.parseInt(this.txtTongTien.getText()));
            try {
                DichVuData.InsertDichVu(p);
            } catch (SQLException ex) {
                Logger.getLogger(frmDichVu.class.getName()).log(Level.SEVERE, null, ex);
            }
        resetForm();
        UpdateTable.LoadData(sqlDg, tblDV);
        }
    }//GEN-LAST:event_btnThemActionPerformed

